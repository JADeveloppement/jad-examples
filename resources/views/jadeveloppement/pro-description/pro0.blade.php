Ceci est mon site web personnel que j'ai commencé à développé en 2014/2015. Je débutais dans l'apprentissage des langages web
et cela représentait pour moi le premier challenge.<br>
Au départ, ce n'était qu'un site développé avec les langages classiques HTML/CSS/Javascript.<br>
Les années passant, et les connaissances s'améliorant, je l'ai remis au gout du jour (et le mets toujours à jour) et l'ai développé en utilisant
les outils modernes à ma disposition (Laravel, Wordpress, Tailwind, PHP, MySQL, etc)<br>
Actuellement, ce site web est codé en utilisant le framework php Laravel, un framework CSS Tailwind (Sass), et jQuery pour le côté Javascript.
<br>
<br>
<i><u>Langages utilisés :</u> Sass, Javascript, jQuery, PHP, HTML, CSS</i><br>
<i><u>Frameworks :</u> Laravel, Laravel-mix, Tailwind-CSS</i><br>
<i><u>Outils :</u> Ubuntu, VSCode, Git, Jira</i>
