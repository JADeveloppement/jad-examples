<div class="description-text">
    C'est un site web utilisant le CMS Wordpress pour la landing page, et le framework Laravel pour la partie espace client.
    <br>Ce projet était un challenge pour moi car je devais concilier deux méthodes de travail pour développer et la landing page 
    et l'espace client n'utilisant pas les mêmes environnement de développement.
    <br>
    Pour la partie Wordpress, j'ai utilisé les méthodes classiques avec child-theme et functions/style/custom-js personnalisés, pour s'adapter 
    aux besoins spécifiques du client.
    <br>Concernant l'espace client, je l'ai développé en utilisant le framework Laravel que j'ai trouvé adapté pour cette utilisation :
    <ul class="list-disc">
        <li>Il y a une partie Client qui regroupe différents onglets : </li>
        <ul style="list-style-type: circle;" class="ml-[4rem]">
            <li>Un onglet pour gérer ses informations personnelles</li>
            <li>Un onglet pour accéder au contenu pour lequel la personne s'est abonnée</li>
            <li>Un onglet pour accéder à un espace de prise de rendez-vous personnalisé</li>
        </ul>
        <li>Il y a une partie Administrateur qui regroupe aussi différents onglets : </li>
        <ul style="list-style-type: circle;" class="ml-[4rem]">
            <li>Un onglet pour les informations générales du compte administrateur</li>
            <li>Un onglet pour les informations générales et suivi d'inscription des différents clients</li>
            <li>Un onglet pour inscrire les nouveaux utilisateurs et qui créé une page de paiement personnalisée à communiquer au futur client 
                et qui autorisera ou non l'accès au contenu si le paiement a été effectué ou pas (API Paypal)
            </li>
            <li>Un onglet pour accéder à la gestion de rendez-vous personnalisé (en utilisant le plugin Amelia disponible sur Wordpress)</li>
            <li>Un onglet de prévisulation de l'espace client en tant qu'utilisateur lambda</li>
        </ul>
    </ul>
    <br>Cet espace client était un petit challenge, qui m'a beaucoup rappelé un ancien projet développé pour mon compte (Démocrite) car j'ai dû utilisé 
    différentes technologies pour allier un landing page efficace et un espace personnalisé avec système de paiement et gestion de base de donnée avec 
    le stockage des informations générales des clients s'inscrivant et des informations d'administration.
    <br>
