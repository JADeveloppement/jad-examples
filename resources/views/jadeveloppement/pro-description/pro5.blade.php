<div class="description-text">
    Ce site web est un projet personnel que j'ai commencé à développé en 2018 : ce site web consiste en une landing page, avec inscription à un 
    espace client et un système d'abonnement pour profiter de toutes les fonctionnalités de la plateforme.
    <br>
    Au départ, je l'avais développé uniquement en HTML/CSS/PHP/Javascript, mais le temps passant, je l'ai remis au gout du jour en l'adaptant
    aux exigences du développement web d'aujourd'hui.
    <br>Maintenant, il utilise le framework Laravel en backend et Tailwind pour le style, et MySQL pour la gestion de la base de donnée.
    <br>L'espace client est entièrement codé en html/php/css à l'aide de Tailwind, et j'utilise l'API de Stripe&copy; pour le système de paiement.
    <br>MySQL est utilisé pour stocker les informations utilisateurs et pour gérer l'avancement du travail des étudiants inscrits sur la plateforme.
    <br>La boutique, que j'ai retiré (car non utile à ce jour) était entièrement stylisé et codé en HTML/CSS/PHP/Javascript, utilisant 
    d'abord l'API Paypal, que j'ai préféré remplacer par Stripe.
    <br>
